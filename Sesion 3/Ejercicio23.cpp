/* Razone la veracidad y la falsedad de las siguientes afirmaciones:

a) 'c' es una expresión de caracteres

	Verdadero. 'c' es una expresión de un sólo caracter que va entre comillas simples y el 
	tipo de dato de la variable seria char.

b) 4<3 es una expresión numérica 

	Falso. 4<3 es una expresión booleana (que devuelve los valores True o False) en este caso
	seria false ya que 4 no es menor que 3.

c) (4+3)<5 es una expresión numérica

	Falso. (4+3)<5 sigue siendo una expresión booleana aunque en este caso primero resolvería los paréntesis 
	y devolvería false ya que 7 no es menor que 5

d) cout << a; da como salida la escritura en pantalla de una a 

	Falso. Dependiendo de si se pasa a cout un dato de tipo char, imprime el carácter correspondiente
	al entero almacenado pero si se pasa a cout un dato de tipo entero (distinto a char), imprime
	el entero almacenado. Para que imprima 'a' debe ir entre comillas o estar asignado a una variable de tipo char.
	
e) ¿Qué realiza cin >> cte, siendo cte una constante entera?

	Da un error de compilación porque el valor de una constante se asigna cuando se declara y por tanto no se puede
	asignar ni modificar a lo largo del programa. */
	


	
	

	


